def limpiar_descripcion(desc):
    for patron, reemplazo in REEMPLAZOS:
        desc = re.sub(patron, reemplazo, desc, flags=re.IGNORECASE)
    desc = desc.replace('–', '-').replace('—', '-')
    desc = desc.replace('demasiado', 'muy')
    desc = desc.replace('  ', ' ')
    return desc.strip().capitalize()

def recomendar(codigo, desc=''):
    # 1. Específica exacta por código
    if codigo in REC_ESPECIFICA:
        return REC_ESPECIFICA[codigo]
    # 2. Patrón de rango
    for patron, rec in REC_PATRON:
        if re.match(patron, codigo):
            return rec
    # 3. Palabras clave en descripción
    for patron, rec in REC_DESC:
        if re.search(patron, desc, re.IGNORECASE):
            return rec
    # 4. Genérico por tipo
    if codigo.startswith('B'):
        return 'Revisar sistemas de carrocería, sensores y cableado asociado'
    if codigo.startswith('C'):
        return 'Revisar sistema de chasis, ABS, suspensión o dirección'
    if codigo.startswith('U'):
        return 'Revisar red CAN/BUS, módulos de control y cableado'
    return 'Revisar cableado, conectores y componente indicado en el código'

def main():
    with open('fallas_es.json', 'r', encoding='utf-8') as f:
        fallas = json.load(f)
    # Separar por categoría: P, B, C, U
    categorias = {'P': {}, 'B': {}, 'C': {}, 'U': {}}
    for cod, desc in fallas.items():
        desc_limpia = limpiar_descripcion(desc)
        rec = recomendar(cod, desc)
        letra = cod[0].upper() if cod else 'P'
        if letra not in categorias:
            letra = 'P'
        categorias[letra][cod] = {"d": desc_limpia, "r": rec}

    archivos = {
        'P': 'fallas_P.json',
        'B': 'fallas_B.json',
        'C': 'fallas_C.json',
        'U': 'fallas_U.json',
    }
    total_global = 0
    for letra, datos in categorias.items():
        if datos:
            with open(archivos[letra], 'w', encoding='utf-8') as f:
                json.dump(datos, f, ensure_ascii=False, indent=2)
            total_global += len(datos)
            print(f"✅ Generado {archivos[letra]} ({len(datos)} códigos)")

    fallas_opt = {k: v for d in categorias.values() for k, v in d.items()}
    especificos = sum(1 for c in fallas_opt if c in REC_ESPECIFICA)
    print(f"   Total códigos   : {total_global}")
    print(f"   Con rec. exacta : {especificos}")
    print(f"   Con rec. patrón : {total_global - especificos}")

if __name__ == '__main__':
    main()
